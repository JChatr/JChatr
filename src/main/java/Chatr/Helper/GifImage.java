package Chatr.Helper;


import javafx.scene.image.ImageView;

public class GifImage extends ImageView {
	private int width;
	private int height;

	public void setWidth(int width) {
		this.width = width;
	}

	public int getWidth() {
		return this.width;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getHeight() {
		return this.height;
	}
}